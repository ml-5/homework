#include <stdio.h>
int k = 11;
float L[4] = {8.02, 7.43, 4.57, 5.39};
int check(float mid, float *L);
float CutRope(int n);
int main()
{
    printf("%0.2f", CutRope(1000));
}
float CutRope(int n)
{
    float l, r = n, mid;
    while (r - l > 0.001)
    {
        mid = (l + r) / 2;
        if (check(mid, L))
            l = mid;
        else
            r = mid - 1;
    }
    return l;
}
int check(float mid, float *L)
{
    int sum = 0;
    for (int i = 0; i < 4; i++)
    {
        sum += (int)L[i] / mid;
    }
    return sum >= k ? 1 : 0;
}
